      <section class="mi-column mi-width-100">
        <h2 id="what-is-scalp">WHAT IS SCALP MICROPIGMENTATION</h2>
        <p class="mi-width-90">Scalp Micropigmentation is safe, non-surgical and non-invasive treatment, which is affordable solution for restoring confidence in both male and female individuals, who suffer from hair loss. Through detailed micro-needles, organic pigment is deposited at the epidermal level of the scalp, naturally replicating the appearance of real shaved hair follicle. This effect is 100% natural looking and is not recognizable as a hair simulation. By accurately replicating your own natural hair follicles and hair color, scalp micropigmentation can add density to thinning areas, creating an undetectable illusion of thicker, stronger and fuller hair. Further to that SMP is affordable solution for covering scars, received from previous hair transplant surgery or any other reason. In most cases, an SMP treatment lasts for three to five years before the patient needs to have another treatment done because of the fading of the pigments.</p>
