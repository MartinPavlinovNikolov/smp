      <section class="mi-column mi-width-100">
        <h2>DOES IT HURT?</h2>
        <p class="mi-width-90">Micro pigmentation is a non-invasive treatment, therefore there is very little or no pain experienced. It is possible the appearance of a mild skin redness, due to denser scalp nerve endings in some patients, which however should settle within a few hours following the treatment. There is no need for a special maintenance once treatments are complete. The procedure does not affect daily routine and activities. A complete aftercare guide is provided throughout the treatment sessions.</p>
      </section>
      <section class="mi-column mi-width-100">
        <h2>IS SMP JUST FOR MAN?</h2>
        <div class="gallery2-wrapper">
          <p class="gallery2-text">Scalp micropigmentation is suitable for both men and women however, there is a difference between SMP for men and women. Like men, women also can suffer from hair loss or thinning hair. In most of the cases however, women tend to lose hair evenly and is very unlikely that they go completely bald. SMP is used to restore the denser look of healthy hair and reduce the contrast between the scalp and the hairline in this case. While most of the male patients would require to shave down their hair prior to the treatment, this is not necessary for female patients. Instead, the practitioner applies pigmentation by parting the hair section by section.</p>
